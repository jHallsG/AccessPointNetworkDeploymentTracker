package com.tracker.entities;

public class LocationEntity {
	
	private int locId;
	private String locName;
	
	public int getLocId() {
		return locId;
	}
	public String getLocName() {
		return locName;
	}
	public void setLocId(int locId) {
		this.locId = locId;
	}
	public void setLocName(String locName) {
		this.locName = locName;
	}
}
